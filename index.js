countries.isSupported = function(name) {
  return countries.hasOwnProperty(name);
};

countries.getAbbreviation = function(name) {
  return countries[name];
};

module.exports = {
  createStream: function() {
    return through2.obj(function(doc, enc, next) {
      var name = doc.getAdmin('admin0');

      if (countries.isSupported(name)) {
        doc.setAlpha3(countries.getAbbreviation(name));
      }

      this.push(doc);
      next();
    });
  }
};
